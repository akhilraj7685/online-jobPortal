
function showEle(pp) {

    var t=document.getElementById(pp);
    if(t.style.display!=='block'){
        t.style.display="block";
    }


}


function hideEle(pp) {

    var t=document.getElementById(pp);
    if(t.style.display=="block"){
        t.style.display="none";
    }
    return;

}



